## variables and data types ##
name = 'Carla'
last_name = 'Massat'
full = name + " " + last_name
print('Hello, ' + full.title() + '!')  # titlecase

print(full.lower()) # lowercase
print(full.upper()) # uppercase

message = 'I like programming in: \n\tpython \n\tC++'
print(message) # Adding Whitespace to Strings with Tabs or Newlines

age = 32
message_age = 'Im ' + str(age) + 'years old'
print(message_age)

float = 3.0 / 2
print(float)

